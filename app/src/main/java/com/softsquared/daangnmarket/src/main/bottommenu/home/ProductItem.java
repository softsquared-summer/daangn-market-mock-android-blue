package com.softsquared.daangnmarket.src.main.bottommenu.home;

public class ProductItem {
    private int productImage;
    private String productName;
    private String productAddress;
    private String productUpdate;
    private String productPrice;
    private int productComment;
    private int productChat;
    private int productHeart;

    public int getProductImage() {
        return productImage;
    }

    public void setProductImage(int productImage) {
        this.productImage = productImage;
    }

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public String getProductAddress() {
        return productAddress;
    }

    public void setProductAddress(String productAddress) {
        this.productAddress = productAddress;
    }

    public String getProductUpdate() {
        return productUpdate;
    }

    public void setProductUpdate(String productUpdate) {
        this.productUpdate = productUpdate;
    }

    public String getProductPrice() {
        return productPrice;
    }

    public void setProductPrice(String productPrice) {
        this.productPrice = productPrice;
    }

    public int getProductComment() {
        return productComment;
    }

    public void setProductComment(int productComment) {
        this.productComment = productComment;
    }

    public int getProductChat() {
        return productChat;
    }

    public void setProductChat(int productChat) {
        this.productChat = productChat;
    }

    public int getProductHeart() {
        return productHeart;
    }

    public void setProductHeart(int productHeart) {
        this.productHeart = productHeart;
    }
}
